<template>
  <div :class="[gridClasses, $attrs.class]">
    <slot />
  </div>
</template>

<script setup lang="ts">
import { computed } from 'vue'

defineOptions({
  inheritAttrs: false
})

const props = defineProps({
  cols: { type: [Number, String], default: 3 },   // 2,3,4â€¦
  gap : { type: [Number, String], default: 4 },   // Tailwind gap-x
  mt  : { type: [Number, String], default: 0 },   // Tailwind mt-x
})

const gridClasses = computed(() => {
  const colsClass = {
    1: 'grid-cols-1',
    2: 'grid-cols-2', 
    3: 'grid-cols-3',
    4: 'grid-cols-4',
    5: 'grid-cols-5',
    6: 'grid-cols-6'
  }[props.cols] || 'grid-cols-3'
  
  const gapClass = {
    1: 'gap-1',
    2: 'gap-2',
    3: 'gap-3', 
    4: 'gap-4',
    5: 'gap-5',
    6: 'gap-6',
    8: 'gap-8'
  }[props.gap] || 'gap-4'
  
  const mtClass = {
    0: 'mt-0',
    1: 'mt-1',
    2: 'mt-2',
    3: 'mt-3', 
    4: 'mt-4',
    5: 'mt-5',
    6: 'mt-6',
    8: 'mt-8',
    10: 'mt-10',
    12: 'mt-12',
    16: 'mt-16',
    20: 'mt-20'
  }[props.mt] || 'mt-0'
  
  return `grid ${colsClass} ${gapClass} ${mtClass}`
})
</script>


